namespace WZIMopoly.Enums
{
    /// <summary>
    /// Represents the game status.
    /// </summary>
    enum GameStatus
    {
        /// <summary>
        /// The game is running.
        /// </summary>
        Running,

        /// <summary>
        /// The game is paused.
        /// </summary>
        Paused,

        /// <summary>
        /// The game has not started and players are in the lobby.
        /// </summary>
        InLobby,

        /// <summary>
        /// The game is finished.
        /// </summary>
        Finished
    }
}

